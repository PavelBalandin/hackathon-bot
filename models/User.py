class User:
    def __init__(self, user_id, username, first_name, last_name, status_code, post):
        self.user_id = user_id
        self.username = username
        self.first_name = first_name
        self.last_name = last_name
        self.status_code = status_code
        self.post = post
        self.phone = ""
        self.token = ""
